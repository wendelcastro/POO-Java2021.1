
package estruturacondicional;

public class SwitchCase {
    
    public static void main(String[] args) {
       int day = 4;
       switch(day){
           case 1:
               System.out.println("Monday");
               break;
           case 2:
               System.out.println("tuesday");
               break;
           case 3:
               System.out.println("wednesday");
               break;
           case 4:
               System.out.println("Thursday");
               break;
           default:
               System.out.println("");
               
       }
    }
    
}
